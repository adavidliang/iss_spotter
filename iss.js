const request = require('request');



//It will contain most of the logic for fetching the data from each API endpoint.
/**
 * Makes a single API request to retrieve the user's IP address.
 * Input:
 *   - A callback (to pass back an error or the IP string)
 * Returns (via Callback):
 *   - An error, if any (nullable)
 *   - The IP address as a string (null if error). Example: "162.245.144.188"
 */
const fetchMyIP = (callback) => {
  // use request to fetch IP address from JSON API
  request('https://api64.ipify.org?format=json', (error, response, body) => {
    if (error) {
      callback(error, null);
      return;
    }

    // if non-200 status, assume server error
    if (response.statusCode !== 200) {

      const msg = `Status Code ${response.statusCode} when fetching IP. Response: ${body}`;
      callback(Error(msg), null);

      return;
    }

    const ip = JSON.parse(body).ip;
    callback(null, ip);
  });
};

const fetchCoordsByIP = (ip, callback) => {
  request(`https://ipwho.is/${ip}
  `, (error, response, body) => {
    if (error) {
      callback(error, null);
      return;
    }

    let parseBody = JSON.parse(body);

    if (parseBody.success === false) {
      const msg = parseBody.message;
      return callback(msg, null);
    }
    const LALO = { latitude: parseBody.latitude, longitude: parseBody.longitude };
    callback(null, LALO);
  });
};
